c
c minmod limiter 2D
c
      
      subroutine limiter(r,fi)
      
      implicit none
      
      real r(2), fi(2)
      
      if (r(1).gt.0) then
          fi(1)=min(r(1),1.)
      else
          fi(1)=0
      endif
      
      if (r(2).gt.0) then
          fi(2)=min(r(2),1.)
      else
          fi(2)=0
      endif
      
      end 

